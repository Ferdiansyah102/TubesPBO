    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {           
            Transaksi trn = new Transaksi();

            trn.setIdTransaksi(Integer.parseInt(txtidTransaksi.getText()));
            Penumpang penumpang = new Penumpang().getById(Integer.parseInt(txtcariPenumpang.getText()));
            trn.setPenumpang(penumpang);
            Pesawat pesawat = new Pesawat().getById(Integer.parseInt(CombocariPesawat.getToolTipText()));
            trn.setPesawat(pesawat);
            trn.setJumlahTiket(Integer.parseInt(txtjumlahTiket.getText()));
            trn.setTotalBayar(Double.parseDouble(txttotalBayar.getText()));
            trn.save();
            txtidTransaksi.setText(Integer.toString(trn.getIdTransaksi()));
            tampilkanData();
        } catch (Exception e) {
            System.out.println("Exception :" + e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btncariPenumpangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariPenumpangActionPerformed
        // TODO add your handling code here:
        Penumpang penumpang = new Penumpang();
        penumpang = penumpang.getById(Integer.parseInt(txtcariPenumpang.getText()));
        if (String.valueOf(penumpang.getNamaPenumpang()).equalsIgnoreCase("null")) {
            lblpenumpang.setText("Data tidak ditemukan");
        } else {
            lblpenumpang.setText(String.valueOf(penumpang.getNamaPenumpang()));
        }
    }//GEN-LAST:event_btncariPenumpangActionPerformed

