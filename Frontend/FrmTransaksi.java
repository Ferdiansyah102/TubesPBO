    public final void tampilkanData(){
        String[] kolom = {"ID Transaksi","ID Penumpang","ID Pesawat", "Jumlah Tiket", "Total bayar"};
        ArrayList<Transaksi> list = new Transaksi().getAll();
        Object rowData[] = new Object[5];
        
        tblTransaksi.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Transaksi trn : list) {
            rowData[0] = trn.getIdTransaksi ();
            rowData[1] = trn.getPenumpang().getIdPenumpang ();
            rowData[2] = trn.getPesawat().getIdPesawat ();
            rowData[3] = trn.getJumlahTiket();
            rowData[4] = trn.getTotalBayar();
        
            ((DefaultTableModel)tblTransaksi.getModel()).addRow(rowData);
        }
    }
    
    public final void cari(String keyword){
        String[] kolom = {"ID Transaksi","ID Penumpang","ID Pesawat", "Jumlah Tiket", "Total bayar"};
        ArrayList<Transaksi> list = new Transaksi().search(keyword);
        Object rowData[] = new Object[5];
        
        tblTransaksi.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for (Transaksi trn : list) {
            rowData[0] = trn.getIdTransaksi ();
            rowData[1] = trn.getPenumpang().getIdPenumpang ();
            rowData[2] = trn.getPesawat().getIdPesawat ();
            rowData[3] = trn.getJumlahTiket();
            rowData[4] = trn.getTotalBayar();
        
            ((DefaultTableModel)tblTransaksi.getModel()).addRow(rowData);
        }
    }

